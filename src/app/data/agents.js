export function getTodaysAgents(count = 5) {
  const today = new Date();
  const dayOfYear = Math.floor((today - new Date(today.getFullYear(), 0, 0)) / 86400000);
  const cycleLength = 45;
  const agentsPerDay = Math.ceil(AGENTS.length / cycleLength);
  const startIdx = ((dayOfYear % cycleLength) * agentsPerDay) % AGENTS.length;
  const result = [];
  for (let i = 0; i < count; i++) {
    result.push(AGENTS[(startIdx + i) % AGENTS.length]);
  }
  return result;
}

export function getCycleInfo() {
  const today = new Date();
  const dayOfYear = Math.floor((today - new Date(today.getFullYear(), 0, 0)) / 86400000);
  const cycleDay = (dayOfYear % 45) + 1;
  return { cycleDay, totalAgents: AGENTS.length };
}
